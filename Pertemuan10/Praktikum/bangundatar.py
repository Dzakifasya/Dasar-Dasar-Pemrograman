import math
def persegi(sisi):
    hasil = sisi * sisi
    return hasil

def persegipanjang(panjang, lebar):
    hasil = panjang * lebar
    return hasil

def lingkaran(jari_jari):
    hasil = math.pi * math.pow(jari_jari, 2)
    return hasil

def segitiga(alas, tinggi):
    return(alas * tinggi) / 2

def jajargenjang(alas, tinggi):
    hasil = alas * tinggi
    return hasil